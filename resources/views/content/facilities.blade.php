    <p class="rtejustify"><strong>The unique facility is located in the South West of the UK and offers the research
            community access to a range of </strong></p>
    <p class="rtejustify"><strong><em>in-situ </em>state-of-the-art instrumentation installed over three hydrologically
            isolated farming systems or farmlets. </strong></p>
    <p class="rtejustify"><strong>The platform also has an indoor cattle system that is similarly instrumented.</strong>
    </p>

    <p><img src="/images/facilities01.png" /></p>
    <p></p>
    <p class="rtejustify">Each outdoor farmlet consists of five component sub-catchments comprising just over 20 ha in
        total per farmlet.</p>
    <p class="rtejustify">They are currently managed as beef cattle, sheep and arable enterprises with a high degree of
        control over inputs.</p>
    <p class="rtejustify">The key concept of the Farm Platform is to explore different ways of managing sustainable and
        resilient grazing livestock production alongside a wide range of agro-ecosystem attributes at farm scale.
        Opportunities also exist for investigating net zero strategies.</p>
    <p><img src="/images/facilities02.png" alt="" /></p>
    <p class="rtejustify">The facility attracts researchers from different communities and disciplines to promote new
        ideas or tackle old challenges in novel ways to better address key issues in sustainable and resilient
        agriculture.</p>
    <p class="rtejustify">All core input and output data collected from across the platform is freely available to all
        national and international research users and collaborators via our <a href="https://nwfp.rothamsted.ac.uk/"
            onclick="window.open(this.href, '', 'resizable=yes,status=no,location=yes,toolbar=no,menubar=yes,fullscreen=no,scrollbars=yes,dependent=no'); return false;">Data
            Portal</a>.</p>
    <p class="rtejustify">We have dedicated laboratories <span
            style="line-height: 150%; font-family: &quot;Arial&quot;,sans-serif; font-size: 11pt; mso-fareast-font-family: Calibri; mso-bidi-font-family: &quot;Times New Roman&quot;; mso-ansi-language: EN-GB; mso-fareast-language: EN-US; mso-bidi-language: AR-SA; mso-fareast-theme-font: minor-latin; mso-bidi-theme-font: minor-bidi;">to
            handle soil, herbage, air and water samples and have a wide range of analytical techniques available in our
            Analytical Laboratories.</span></p>
    <p><img src="/images/facilities03.png" /></p>
    <p>In addition, we have the Small Ruminant Facility (SRF; Robert Orr building) which is used to not only house the
        Farm Platform sheep during the winter period but which also contains 24 automated biocontrol units. These units
        allow the recording of individual feed intake using automatically weighed feed hoppers and which are available
        for studies concerning animal health, welfare and performance. Some pens within the facility have also been
        specifically designed to accommodate goat housing.</p>
    <p><img src="/images/SmallRuminantFacility.png" style="width: 800px; height: 370px;" /></p>
    <p></p>
    <p><strong><span style="color:#FF0000;">We welcome opportunities for co-development of projects on the platform.
                Project proposals can be of any size and through any funding body.</span></strong></p>
    <p><span style="color:#FF0000;"><strong>If you are interested in proposing or conducting a research project using
                the Farm Platform facilities, please contact the North Wyke Farm Platform team</strong></span> (<a
            href="mailto:nw.farmplatform@rothamsted.ac.uk">nw.farmplatform@rothamsted.ac.uk</a>) <span
            style="color:#FF0000;"><strong>for initial discussions, where we will align you with the contacts and
                expertise to facilitate your proposal or project. This is the first step, where the second step
                (following initial consultation) is to complete the farm platform access form in the Downloads section
                and submit for approval.</strong></span></p>
    <p>Our facilities are part-funded by CIEL (<a href="https://www.cielivestock.co.uk/"
            onclick="window.open(this.href, '', 'resizable=yes,status=no,location=yes,toolbar=no,menubar=yes,fullscreen=no,scrollbars=yes,dependent=no'); return false;">https://www.cielivestock.co.uk/</a>).
        As such, if you are considering applying for funding to conduct an NWFP or SRF experiment, then please contact
        us so that we can ensure a small charge (typically 2%) is captured in the funding application reflecting CIEL's
        investment.</p>
